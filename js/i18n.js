let currentLang = localStorage.getItem('normandy-lang') || 'en';

/** Detect page slug from URL */
function detectPage() {
  const path = window.location.pathname;
  const file = path.split('/').pop().replace('.html','') || 'index';
  return file === '' ? 'index' : file;
}

/** Merge shared + page-specific translations for the current lang */
function getDict(lang) {
  const page = detectPage();
  const pageTr = (pageTranslations[page] || {})[lang] || {};
  return Object.assign({}, sharedTranslations[lang], pageTr);
}

/** Apply translations to all [data-i18n] elements */
function applyLang(lang) {
  const dict = getDict(lang);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  /* Update toggle button on every page */
  const btn  = document.getElementById('langToggleBtn');
  const flag  = document.getElementById('langFlag');
  const label = document.getElementById('langLabel');
  if (btn && flag && label) {
    flag.textContent  = lang === 'en' ? '🇫🇷' : '🇬🇧';
    label.textContent = lang === 'en' ? 'Français' : 'English';
  }

  document.documentElement.lang = lang;
}

/** Toggle and persist */
function toggleLanguage() {
  currentLang = currentLang === 'en' ? 'fr' : 'en';
  localStorage.setItem('normandy-lang', currentLang);

  const btn = document.getElementById('langToggleBtn');
  if (btn) {
    btn.classList.add('switching');
    setTimeout(() => btn.classList.remove('switching'), 350);
  }
  applyLang(currentLang);
}

/* Apply on DOM ready */
document.addEventListener('DOMContentLoaded', () => applyLang(currentLang));
